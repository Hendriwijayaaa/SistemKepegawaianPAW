    <script>
        // Function to handle menu link click
        function handleMenuClick(event) {

            const clickedMenuItem = event.currentTarget; // Get the clicked menu item

            // Remove active class from all menu links
            const menuLinks = document.querySelectorAll('.menu-link');
            menuLinks.forEach(link => link.classList.remove('active'));

            // Add active class to the clicked menu link
            clickedMenuItem.classList.add('active');

            // Save active menu link to storage
            localStorage.setItem('activeMenu', clickedMenuItem.getAttribute('href'));
        }

        // Function to set active menu link based on stored value
        function setActiveMenuFromStorage() {
            const activeMenu = localStorage.getItem('activeMenu');
            if (activeMenu) {
                // Remove active class from all menu links
                const menuLinks = document.querySelectorAll('.menu-link');
                menuLinks.forEach(link => link.classList.remove('active'));

                // Add active class to the menu link stored in localStorage
                const activeMenuItem = document.querySelector(`.menu-link[href="${activeMenu}"]`);
                if (activeMenuItem) {
                    activeMenuItem.classList.add('active');
                }
            }
        }

        // Add click event listeners to menu links
        document.querySelectorAll('.menu-link').forEach(link => {
            link.addEventListener('click', handleMenuClick);
        });

        // Set active menu from storage on page load
        setActiveMenuFromStorage();
    </script>


